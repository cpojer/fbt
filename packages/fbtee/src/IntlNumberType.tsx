export type LangToNumberTypeValues =
  (typeof langToNumberType)[keyof typeof langToNumberType];
export type LocaleToNumberTypeValues =
  (typeof localeToNumberType)[keyof typeof localeToNumberType];

function _getNumberModuleForLang(lang: string): LangToNumberTypeValues {
  return (
    langToNumberType[lang as unknown as keyof typeof langToNumberType] ||
    IntlCLDRNumberType01
  );
}

function _getNumberModuleForLocale(
  locale: string,
): LocaleToNumberTypeValues | LangToNumberTypeValues {
  return (
    localeToNumberType[locale as unknown as keyof typeof localeToNumberType] ||
    _getNumberModuleForLang(FBLocaleToLang.get(locale))
  );
}

function forLocale(
  locale: string,
): LocaleToNumberTypeValues | LangToNumberTypeValues {
  return _getNumberModuleForLocale(locale);
}

export default {
  forLanguage(language: string): LangToNumberTypeValues {
    return _getNumberModuleForLang(language);
  },
  forLocale,
  get: forLocale,
};
